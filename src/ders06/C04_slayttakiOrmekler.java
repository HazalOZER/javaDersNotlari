package ders06;

public class C04_slayttakiOrmekler {
    public static void main(String[] args) {
        int sayi=10;
        System.out.println(sayi++);//Önce sayıyı yazdırır(10) sonra değer, arttoror
        System.out.println(sayi);//Üst satırda değer (11) oldu,(11) yazdırır

        int sayi1=10;
        System.out.println(++sayi1);//Önce sayıyı arttırır(11) sonra yazdıır(11)
        System.out.println(sayi1);// Üst satırdali değer (11) oldu (11) yazdırır

    }
}
