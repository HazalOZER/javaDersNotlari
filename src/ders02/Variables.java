package ders02;

public class Variables {
    public static void main(String[] args) {

        int sayi; //deklarasyon sadece bir kere yapılır
        sayi=15; //değer ataması istediğimiz kadar yapılır
        System.out.println(sayi); //15

        sayi=20;

        System.out.println(sayi); //20

        sayi = sayi+10;
        // 20(eski sayi değeri) + 10
        //sayi=30
        System.out.println(sayi); //30


    }
}
