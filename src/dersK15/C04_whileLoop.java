package dersK15;

public class C04_whileLoop {
    public static void main(String[] args) {

        //iki basamaklı sayıları aralarındabir boşluk olacak iekilde yan yana yazdırın

        for (int i = 10; i < 100 ; i++) {
            System.out.print(i+" ");

        }
        System.out.println("");
        int deger=10;

        while (deger<100){
            System.out.print(deger+" ");
        deger++;
        }

    }
}
